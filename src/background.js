class Background {
  constructor(canvas, width, height, color = '#eee', display: block) {
    this.canvas = canvas;
    this.width = width;
    this.height = height;
    this.color = color;
    this.display = display;
  }
  render(ctx) {
   ctx.beginPath();
   ctx.canvas;
   ctx.display;
   ctx.color   
   ctx.closePath; 
  }


  

}
export default Background;
